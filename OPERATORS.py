# 2. Операторы сравнения:
# ```python
a = 5
b = 3

# Меньше
if a < b:
    print("a меньше b")

# Больше
if a > b:
    print("a больше b")

# Меньше или равно
if a <= b:
    print("a меньше или равно b")

# Больше или равно
if a >= b:
    print("a больше или равно b")

# Равно
if a == b:
    print("a равно b")

# Не равно
if a != b:
    print("a не равно b")

# 3. Логические операторы:
# ```python
a = 5
b = 3

# И
if a > 3 and b > 3:
    print("Оба числа больше 3")

# Или
if a > 3 or b > 3:
    print("Хотя бы одно число больше 3")

# Не
if not (a > 3):
    print("Число a не больше 3")

# 4. Операторы присваивания:
# ```python
a = 5

# Присвоить
b = a  # Теперь b равно 5

# Прибавить и присвоить
a += 3  # Теперь a равно 8

# Вычесть и присвоить
a -= 3  # Теперь a равно 5

# Умножить и присвоить
a *= 3  # Теперь a равно 15

# Разделить и присвоить
a /= 3  # Теперь a равно 5.0

# Взять остаток и присвоить
a %= 3  # Теперь a равно 2```

